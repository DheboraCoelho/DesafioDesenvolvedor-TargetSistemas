package com.mycompany.sequencias;

/**
 *
 * @author Dhebora
 */
public class Sequencias {
    public static void main(String[] args) {
        // a) 1, 3, 5, 7, ___
        int[] seqA = {1, 3, 5, 7, 9};
        System.out.print("a) ");
        for (int num : seqA) {
            System.out.print(num + " ");
        }
        System.out.println();

        // b) 2, 4, 8, 16, 32, 64, ____
        int[] seqB = {2, 4, 8, 16, 32, 64, 128};
        System.out.print("b) ");
        for (int num : seqB) {
            System.out.print(num + " ");
        }
        System.out.println();

        // c) 0, 1, 4, 9, 16, 25, 36, ____
        int[] seqC = {0, 1, 4, 9, 16, 25, 36, 49};
        System.out.print("c) ");
        for (int num : seqC) {
            System.out.print(num + " ");
        }
        System.out.println();

        // d) 4, 16, 36, 64, ____
        int[] seqD = {4, 16, 36, 64, 100};
        System.out.print("d) ");
        for (int num : seqD) {
            System.out.print(num + " ");
        }
        System.out.println();

        // e) 1, 1, 2, 3, 5, 8, ____
        int[] seqE = {1, 1, 2, 3, 5, 8, 13};
        System.out.print("e) ");
        for (int num : seqE) {
            System.out.print(num + " ");
        }
        System.out.println();

        // f) 2, 10, 12, 16, 17, 18, 19, ____
        int[] seqF = {2, 10, 12, 16, 17, 18, 19, 20};
        System.out.print("f) ");
        for (int num : seqF) {
            System.out.print(num + " ");
        }
        System.out.println();
    }
}

